def grupo_de_numeros(numero):
    if numero > 0:
        return "El numero es Positivo"
    elif numero < 0:
        return "El numero es Negativo"
    else:
        numero == 0
        return "El numero es Cero"

print(grupo_de_numeros(10))
print(grupo_de_numeros(0))
print(grupo_de_numeros(-5))
